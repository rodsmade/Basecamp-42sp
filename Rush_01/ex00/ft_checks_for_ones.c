void	ft_checks_for_ones(char *g_mapa, char hor[2][4], char ver[4][2])
{
	int i;

	i = -1;
	while (++i < 4)
	{
		if (ver[i][0] == '1')
			*(g_mapa + (i * 4 + 0)) = '4';
		if (ver[i][1] == '1')
			*(g_mapa + (i * 4 + 3)) = '4';
		if (hor[0][i] == '1')
			*(g_mapa + (0 * 4 + i)) = '4';
		if (hor[1][i] == '1')
			*(g_mapa + (3 * 4 + i)) = '4';
	}
}
