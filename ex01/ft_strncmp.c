int	ft_strncmp(char *s1, char *s2, unsigned int n)
{
	unsigned int a;

	a = 0;
	if (n == 0)
		return (0);
	while (s1[a] == s2[a] && s1[a] != '\0' && (a + 1 < n))
	{
		a++;
		if (s2[a] < 0)
			s2[a] = s2[a] * (-1);
	}
	return ((s1[a]) - (s2[a]));
}
