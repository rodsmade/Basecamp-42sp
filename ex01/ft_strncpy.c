char	*ft_strncpy(char *dest, char *src, unsigned int n)
{
	char	*inicio_dest;

	inicio_dest = dest;
	while (*src != '\0' && n > 0)
	{
		*dest = *src;
		dest++;
		src++;
		n--;
	}
	while (n > 0)
	{
		*dest = '\0';
		dest++;
		n--;
	}
	return (inicio_dest);
}
